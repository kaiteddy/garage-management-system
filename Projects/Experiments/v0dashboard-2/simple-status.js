require('dotenv').config({ path: '.env.local' });
const { neon } = require('@neondatabase/serverless');
const fs = require('fs');
const path = require('path');

async function simpleStatus() {
  console.log('üîç SIMPLE STATUS CHECK');
  console.log('======================');
  
  try {
    // 1. Database connection
    const sql = neon(process.env.DATABASE_URL);
    const result = await sql`SELECT NOW() as time, version() as version`;
    console.log('‚úÖ Database: CONNECTED');
    console.log('üìÖ Time:', result[0].time);
    
    // 2. Check main tables
    const tables = await sql`
      SELECT table_name FROM information_schema.tables 
      WHERE table_schema = 'public' 
      ORDER BY table_name
    `;
    console.log('üìã Tables:', tables.length, 'found');
    
    // 3. Check record counts
    const mainTables = ['customers', 'vehicles', 'documents'];
    for (const table of mainTables) {
      try {
        const count = await sql.unsafe(`SELECT COUNT(*) as count FROM ${table}`);
        console.log(`üìä ${table}:`, count[0].count, 'records');
      } catch (e) {
        console.log(`‚ùå ${table}: Not found`);
      }
    }
    
    // 4. Check data files
    const dataDir = path.join(__dirname, 'data');
    if (fs.existsSync(dataDir)) {
      const files = fs.readdirSync(dataDir);
      console.log('üìÅ Data files:', files.length);
      files.forEach(f => console.log('  -', f));
    } else {
      console.log('‚ùå No data directory');
    }
    
    console.log('‚úÖ STATUS CHECK COMPLETE');
    
  } catch (error) {
    console.log('‚ùå Error:', error.message);
  }
}

simpleStatus();
