require('dotenv').config({ path: '.env.local' });
const { neon } = require('@neondatabase/serverless');

console.log('üîç DATABASE STATUS CHECK');
console.log('========================');
console.log('‚è∞ Time:', new Date().toLocaleTimeString());
console.log('');

async function checkStatus() {
  try {
    console.log('1Ô∏è‚É£ Testing connection...');
    const sql = neon(process.env.DATABASE_URL, {
      connectionTimeoutMillis: 5000,
      queryTimeoutMillis: 5000
    });
    
    const connectionTest = await sql`SELECT NOW() as current_time, version() as db_version`;
    console.log('   ‚úÖ Connection: WORKING');
    console.log('   üìÖ Time:', connectionTest[0].current_time);
    console.log('   üóÑÔ∏è  Version:', connectionTest[0].db_version.split(' ')[0]);
    console.log('');
    
    console.log('2Ô∏è‚É£ Checking tables...');
    const tables = await sql`
      SELECT table_name
      FROM information_schema.tables
      WHERE table_schema = 'public'
      ORDER BY table_name
    `;
    
    console.log('   üìã Tables found:', tables.length);
    console.log('   üìù Table names:', tables.map(t => t.table_name).join(', '));
    console.log('');
    
    console.log('3Ô∏è‚É£ Checking record counts...');
    const mainTables = ['customers', 'vehicles', 'documents'];
    const counts = {};
    
    for (const tableName of mainTables) {
      try {
        const result = await sql.unsafe(`SELECT COUNT(*) as count FROM ${tableName}`);
        counts[tableName] = parseInt(result[0].count);
        console.log(`   üìä ${tableName}: ${counts[tableName]} records`);
      } catch (e) {
        counts[tableName] = 'Table not found';
        console.log(`   ‚ùå ${tableName}: Table not found`);
      }
    }
    console.log('');
    
    console.log('4Ô∏è‚É£ Checking data directory...');
    const fs = require('fs');
    const path = require('path');
    
    const dataDir = path.join(__dirname, 'data');
    if (fs.existsSync(dataDir)) {
      const files = fs.readdirSync(dataDir);
      const csvFiles = files.filter(f => f.endsWith('.csv'));
      console.log('   üìÅ Data directory: EXISTS');
      console.log('   üìÑ Total files:', files.length);
      console.log('   üìä CSV files:', csvFiles.length);
      
      if (csvFiles.length > 0) {
        console.log('   üìã CSV files found:');
        csvFiles.forEach(file => {
          const filePath = path.join(dataDir, file);
          const stats = fs.statSync(filePath);
          const sizeKB = Math.round(stats.size / 1024);
          console.log(`      - ${file} (${sizeKB}KB)`);
        });
      }
    } else {
      console.log('   ‚ùå Data directory: NOT FOUND');
      console.log('   üìç Expected location:', dataDir);
    }
    console.log('');
    
    console.log('üìä STATUS SUMMARY:');
    console.log('==================');
    console.log('üîó Database Connection:', '‚úÖ WORKING');
    console.log('üìã Tables Available:', tables.length);
    console.log('üë• Customers:', counts.customers || 'N/A');
    console.log('üöó Vehicles:', counts.vehicles || 'N/A');
    console.log('üìÑ Documents:', counts.documents || 'N/A');
    console.log('');
    
    if (fs.existsSync(dataDir)) {
      const files = fs.readdirSync(dataDir);
      const csvFiles = files.filter(f => f.endsWith('.csv'));
      console.log('üéØ READY FOR IMPORT:', csvFiles.length > 0 ? '‚úÖ YES' : '‚ùå NO CSV FILES');
    } else {
      console.log('üéØ READY FOR IMPORT:', '‚ùå NO DATA DIRECTORY');
    }
    
    console.log('');
    console.log('‚úÖ STATUS CHECK COMPLETE!');
    
  } catch (error) {
    console.log('‚ùå Status check failed:', error.message);
    console.log('');
    console.log('üîß Troubleshooting:');
    console.log('   - Check DATABASE_URL in .env.local');
    console.log('   - Verify database is running');
    console.log('   - Check network connectivity');
  }
}

checkStatus();
